    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String item=code.getText();
        {
            try{
                Class.forName("com.mysql.jdbc.Driver");
                Connection connec = DriverManager.getConnection("jdbc:mysql://localhost/project","root",""); 
                String sql="select * from stocks where ItemCode='"+item+"'";
                PreparedStatement stm = connec.prepareStatement(sql);
                ResultSet rs = stm.executeQuery();

                jTable1.setModel(DbUtils.resultSetToTableModel(rs));
                
            }catch (Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
       int index = jTable1.getSelectedRow();
       TableModel model  = jTable1.getModel();
            String ItemCode = model.getValueAt(index, 0).toString();
            String PO = model.getValueAt(index, 1).toString();
            String Description = model.getValueAt(index, 2).toString();
            String Qty = model.getValueAt(index, 3).toString();
            String UOM = model.getValueAt(index, 4).toString();
            String Price = model.getValueAt(index, 5).toString();
            String Department = model.getValueAt(index, 6).toString();
            String Location = model.getValueAt(index, 7).toString();
            
            jtRowData.setVisible(true);
            jtRowData.pack();
            jtRowData.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
            
            jtRowData.itemcode.setText(ItemCode); 
            jtRowData.po.setText(PO); 
            jtRowData.descriptions.setText(Description);
            jtRowData.qtys.setText(Qty);
            jtRowData.uoms.setText(UOM);
            jtRowData.prices.setText(Price);
            jtRowData.departments.setText(Department);
            jtRowData.locations.setText(Location);
    }//GEN-LAST:event_jTable1MouseClicked

